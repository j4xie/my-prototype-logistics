package com.cretas.aims.entity.smartbi.enums;

/**
 * 数据方向枚举
 * 用于自动检测 Excel 数据是行方向还是列方向
 *
 * @author Cretas Team
 * @version 1.0.0
 * @since 2026-01-23
 */
public enum DataOrientation {

    /**
     * 行方向数据（标准表格格式）
     *
     * 特征：
     * - 每行是一条完整记录
     * - 有日期列作为数据列（不是表头）
     * - 数据行数 > 数值列数（长表）
     *
     * 示例：
     * | 日期       | 产品   | 销量 |
     * | 2025-01-01 | 产品A  | 100  |
     * | 2025-01-02 | 产品B  | 150  |
     */
    ROW_ORIENTED,

    /**
     * 列方向数据（需要转置）
     *
     * 特征：
     * - 第一列是文本标签（项目名），后续列是数值
     * - 表头包含时间信息（2025年1月、Q1、Jan等）
     * - 数值列数 > 数据行数（宽表）
     * - 第一行/前几行有日期格式的列名
     *
     * 示例（利润表）：
     * |          | 2025年1月 | 2025年2月 |
     * | 营业收入 |   100     |   110     |
     * | 营业成本 |    60     |    65     |
     */
    COLUMN_ORIENTED,

    /**
     * 无法确定方向
     * 当检测算法无法明确判断时返回此值
     * 此时应使用用户指定的 transpose 参数或默认不转置
     */
    UNKNOWN
}
